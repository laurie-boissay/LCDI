                        <h2>Fonctionnement de <strong>LCDI</strong> :</h2>
                        <article>
                            <h3>Devenir membre de l'association :</h3>
                            <p>
                                Pour devenir membre de LCDI il vous sera demandé une cotisation annuelle de 20€. La cotisation est individuelle. Vous obtiendrez alors une carte de membre.
                            </p>
                            <p>
                                Les nouveaux joueurs peuvent accéder librement à l'association et participer aux activités régulières du club jusqu'à 3 fois avant de prendre une carte de membre. vous pouvez donc tâter l'ambiance avant de décider de vous investir dans la vie de l'asso.
                            </p>

                            <h3>Evénéments :</h3>
                            <p>
                                Le Cercle Des Investigateurs organise régulièrement des événements autour du jeu de société. Ils se tiennent généralement dans les locaux de l'association le dimanche ou le samedi.
                            </p>
                            <p>
                                 Il peut s'agir de tournois, de concours, de rencontres avec des auteurs, des éditeurs de jeux, de découverte de nouveaux formats de jeux avec la participation d'associations extérieures, d'après-midi ou soirée à thème...
                            </p>
                            <p>
                                Ces activités particulières peuvent être réservées aux membres de l'association ou ouvertes au public. Dans tous les cas, une inscription peut être demandée pour respecter la limite de sécurité de 19 personnes dans la salle des sirènes ou pour satisfaire à d'autres contraintes d'organisation.
                            </p>
                            <p>
                                Certains événements sont gratuits car organisés entièrement par le club et sans engagement de frais ou payants si des lots sont en jeu ou qu'une tierse partie participe à l'activité.
                            </p>
                            <p>
                                LCDI peut aussi participer à des événements extérieurs comme la fête de la ville et forum des associations qui a lieu tous les ans en spetembre. A cette occasion, les membres du club tiennent un stand pour présenter nos activités et recruter de nouveaux joueurs.
                            </p>

                            <h3>Devenir membre du bureau :</h3>
                             <p>
                                Les membres du bureau sont responsables des clés, de l'ouverture de l'association et du bon fonctionnement de cette dernière selon leurs rôles respectifs.
                            </p>
                            <p>
                               Pour devenir membre du bureau de LCDI il faut se présenter lors de l'assemblée générale et être élu par la majorité.
                            </p>

